import java.io.*;
import java.util.*;
class Type_Cast
{
  public static void main(String arg[])
{
 int i=20;
  float f= i;
 System.out.println("Value of i ="+i);
 System.out.println("Value of f ="+f);
  int k=50;
  float g=(float)k;
 System.out.println("Value of k ="+k);
 System.out.println("Value of g ="+g);
}
}